
public class BooleanExample {
    public static void main(String[] args) {
        // boolean 타입은 1byte로 표현되는 논리값(true/false)을 저장
        
        boolean stop = true;
        if(stop){
            System.out.println("중지합니다.");
        }else{
            System.out.println("시작합니다.");
        }
    }

}